// Declare a array
var arr = [1, 2, 3, 4, 5];

// Print the array
console.log(arr);

// Get the number of elements in the array
console.log("The Length of the Array is : ",arr.length);

// Accessing elements of the array
console.log(arr[0]);
console.log(arr[1]);
console.log(arr[2]);
console.log(arr[3]);
console.log(arr[4]);

// Updating elements of the array
arr[0] = 10;

// Print the updated array
console.log("The Updated are is : ",arr);

// Iterate over the elements

console.log("Iterating over the array:");
for (var i = 0; i < arr.length; i++) {
  console.log(arr[i]);
}

